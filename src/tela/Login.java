    public void limpaCampos() {
        userInput.setText("");
        senhaInput.setText("");
        userInput.grabFocus();
    }

    public void logar() {
        Querys loginUser = new Querys();
        userStatic = loginUser.LoginUser(userInput.getText(), senhaInput.getText(), jpa);
        if (userStatic != null) {
            JOptionPane.showMessageDialog(null, "Bem vindo: " + userStatic.getNome());
            limpaCampos();
            Principal principal = new Principal();
            principal.setVisible(true);
            this.setVisible(false);

            usuario = userStatic.getNome();
        } else {
            JOptionPane.showMessageDialog(null, "User: " + userInput.getText() + " n√£o encontrado");
            limpaCampos();
            userStatic = null;
        }
    }

